"""
% 表示格式化输出符: 当print函数输出的内容包含多种不同类型的变量,就要对变量进行格式化
%s:字符串
%d:整数
%f:小数
%%:输出%
"""

# 格式化输出字符串
name = "小花"
print("大家好我叫 %s" % name)

# 格式化输出整数(06控制长度,不满6位以0补全)
num = 19
print("我的学号是 %06d" % num)

# 格式化输出小数(.2控制长度,小数点后面保留2位)
price = 4.5
weight = 5
money = price * weight
print("苹果单价 %.2f 元/斤,重量 %d 斤,总价 %.2f 元" % (price, weight, money))  # 多个变量放()里用, 隔开

# 格式化输出百分比
scale = 0.25
print("数据比例是 %.2f%%" % (scale * 100))
